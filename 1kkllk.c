#include <stdio.h>

int main() {
    int A, B, C;

    // Введення значень змінних
    printf("Введіть A, B, C: ");
    scanf("%d %d %d", &A, &B, &C);

    // Виведення змінених значень без використання проміжних змінних
    printf("A = %d\n", C);
    printf("B = %d\n", A);
    printf("C = %d\n", B);

    return 0;
}
